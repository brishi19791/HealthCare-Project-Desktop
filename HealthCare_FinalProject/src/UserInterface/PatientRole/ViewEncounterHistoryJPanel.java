    private void viewjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewjButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = encounterjTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Row");
        } else {
            

            Encounter e = (Encounter) encounterjTable.getValueAt(selectedRow, 0);
            if(e.getMedication().getDrug()==null){
                JOptionPane.showMessageDialog(null,"There is NO drug to view Medication Guide");
                return;
            }
            String sideEffects=e.getMedication().getDrug().getMedicationGuide().getSideEffects();
            String notes=e.getMedication().getDrug().getMedicationGuide().getNotes();
            String donts=e.getMedication().getDrug().getMedicationGuide().getDonts();
            String previous=e.getMedication().getDrug().getMedicationGuide().getPreviousHistory();
            String precautions=e.getMedication().getDrug().getMedicationGuide().getPrecautions();
            System.out.println(sideEffects);
            if (sideEffects==null && notes==null&& donts==null && previous==null && precautions==null ) {
                JOptionPane.showMessageDialog(null,"No Medication Guide for this drug");
                return;
            }
                ViewMedicationGuideJPanel vehjp = new ViewMedicationGuideJPanel(userProcessContainer, account, enterprise, e.getMedication().getDrug().getMedicationGuide());
                userProcessContainer.add("ViewMedicationGuideJPanel", vehjp);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            
        }
    }//GEN-LAST:event_viewjButtonActionPerformed

