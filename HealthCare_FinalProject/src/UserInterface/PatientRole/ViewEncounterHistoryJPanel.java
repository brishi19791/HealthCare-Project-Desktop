    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) encounterjTable.getModel();
        dtm.setRowCount(0);
        Person p = account.getEmployee();
        Patient patient = (Patient) p;

        for (Encounter e : patient.getEncounterList().getEncounterList()) {
            Object[] row = new Object[3];
            row[0] = e;
            System.out.println(e);
            System.out.println(e.getMedication());
            System.out.println(e.getMedication().getDrug());
            row[1] = e.getMedication().getDrug();
            row[2] = e.getDoctor();
            dtm.addRow(row);
        }

    }

