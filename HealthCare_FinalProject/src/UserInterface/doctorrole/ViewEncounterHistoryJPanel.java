    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel) encounterjTable.getModel();
        dtm.setRowCount(0);
        
        for(Encounter encounter:patient.getEncounterList().getEncounterList()){
            Object[] row=new Object[2];
            row[0]=encounter;
            row[1]=encounter.getDoctor();
            dtm.addRow(row);
            
        }
        
    }

