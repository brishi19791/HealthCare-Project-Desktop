    private void viewmedicationjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewmedicationjButtonActionPerformed
        // TODO add your handling code here:
         int selectedRow = encounterjTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Row");
        }
        Encounter encounter = (Encounter) encounterjTable.getValueAt(selectedRow, 0);
        if(encounter.getMedication().getDrug()==null){
            JOptionPane.showMessageDialog(null,"No medication available");
            return;
        }
         ViewMedicationJPanel vmjp = new ViewMedicationJPanel(userProcessContainer, encounter);
        userProcessContainer.add("ViewMedicationJPanel", vmjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewmedicationjButtonActionPerformed

