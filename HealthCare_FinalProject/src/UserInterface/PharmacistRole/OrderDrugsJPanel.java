    private void sendjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendjButtonActionPerformed
        // TODO add your handling code here:
        String message = messagejTextField.getText();
        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter message");
        } else {
            Drug drug=(Drug)drugjComboBox.getSelectedItem();
            Enterprise enterprise1=(Enterprise)drugcompjComboBox.getSelectedItem();
            PharmacistWorkRequest request=new PharmacistWorkRequest();
            request.setMessage(message);
            request.setSender(account);
            request.setStatus("Awaiting");
            request.setDrug(drug);
            
            request.setPharmacy((PharmacyEnterprise) enterprise);
            
            enterprise1.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
            
        }
        
    }//GEN-LAST:event_sendjButtonActionPerformed

