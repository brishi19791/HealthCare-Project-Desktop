    public void populateNetworkcombo(){
        networkjComboBox.removeAllItems();
        for (Network network : system.getNetworkList()) {
            networkjComboBox.addItem(network);
        }
    }
     
    public void populateDrugCompanyCombo(Network network){
        drugcompjComboBox.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if(enterprise.getEnterpriseType().getValue().equals("DrugCompany"))
            drugcompjComboBox.addItem(enterprise);
        }
    }
    
    public void populateDrugsCombo(DrugCompanyEnterprise enterprise){
        drugjComboBox.removeAllItems();
        for(Drug drug:enterprise.getDrugList().getDrugList()){
            drugjComboBox.addItem(drug);
        }
    }
    
    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)pharmacistjTable.getModel();
        dtm.setRowCount(0);
        
        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
           if(request instanceof PharmacistWorkRequest){
            Object[] row = new Object[5];
            row[0] = request;
            row[1]=request.getSender();
            row[2] = request.getReceiver() == null ? null : request.getReceiver();
            row[3] = request.getStatus();
            PharmacistWorkRequest req=(PharmacistWorkRequest)request;
            row[4] = req.getDrug();
            dtm.addRow(row);
           }
        }
    }

