        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Row");
        }
        Patient p = (Patient) patientjTable.getValueAt(selectedRow, 0);
        Encounter encounter=p.getEncounterList().createEncounter();
        
