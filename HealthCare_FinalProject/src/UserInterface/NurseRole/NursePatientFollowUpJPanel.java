    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) patientjTable.getModel();
        dtm.setRowCount(0);

        for (Patient p : hospitalEnterprise.getPatientList().getPatientList()) {

            Object[] row = new Object[6];
            row[0] = p;
            row[1] = p.getLname();
            row[2] = p.getAge();
            row[3] = p.getCity();
            row[4] = p.getEmail();
            row[5] = p.getNum();
            dtm.addRow(row);

        }
    }

