    private void sendjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendjButtonActionPerformed
        // TODO add your handling code here:
        String message = messagejTextField.getText();
        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Message");
        } else {
            NurseWorkRequest request = new NurseWorkRequest();
            Person doctor = (Person) docnamesjComboBox.getSelectedItem();
            
            request.setMessage(message);
            request.setStatus("sent");
            request.setSender(account);
            request.setPatient(p);
            encounter.setDoctor((Doctor)doctor);
            request.setEncounter(encounter);
            request.setDoctor((Doctor) doctor);
            p.setPrimaryDoctor((Doctor)doctor);
            
            Organization org = null;
            for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (o instanceof DoctorOrganization) {
                    org = o;
                    break;
                }
            }
            if (org != null) {
                for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
                    if (ua.getEmployee().equals(doctor)) {
                        ua.getWorkQueue().getWorkRequestList().add(request);
                        account.getWorkQueue().getWorkRequestList().add(request);
                    }
                }
            }
        }
    }//GEN-LAST:event_sendjButtonActionPerformed

