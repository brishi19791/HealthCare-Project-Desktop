    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) nursejTable.getModel();
        dtm.setRowCount(0);
        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[5];
            row[0] = request;
            row[1] = request.getReceiver() == null ? null : request.getReceiver();
            row[2] = request.getSender();
            row[3] = request.getStatus();
            NurseWorkRequest req = (NurseWorkRequest) request;
            row[4] = req.getPatient().getFname();
            dtm.addRow(row);
        }

    }

    public void populateCombo() {
        docnamesjComboBox.removeAllItems();
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof DoctorOrganization) {
                for (Person p : org.getEmployeeDirectory().getPersonList()) {
                    docnamesjComboBox.addItem(p);
                }
            }
        }
    }

