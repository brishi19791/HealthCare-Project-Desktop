    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (usernamejTextField.getText().isEmpty() || passwordjTextField.getText().isEmpty() || namejTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all details");
        } else {
            Enterprise enterprise = (Enterprise) enterprisenamejComboBox.getSelectedItem();
            String username = usernamejTextField.getText();
            String password = passwordjTextField.getText();
            String name = namejTextField.getText();
            for (Network network : system.getNetworkList()) {
                for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                    
                        for (UserAccount ua : e.getUserAccountDirectory().getUserAccountList()) {
                            if (ua.getUsername().equals(username)) {
                                JOptionPane.showMessageDialog(null, "A user with same username already exists");
                                return;
                            }
                        
                    }
                }
            }
            Person employee = enterprise.getEmployeeDirectory().createPerson(name);
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Hospital)) {
                UserAccount ua = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new HospitalAdminRole());
            } else if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.DrugCompany)) {
                UserAccount ua = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new DrugCompanyAdminRole());
            }else if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Pharmacy)) {
                UserAccount ua = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new PharmacyAdminRole());
            }
            
            
            populatetable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

