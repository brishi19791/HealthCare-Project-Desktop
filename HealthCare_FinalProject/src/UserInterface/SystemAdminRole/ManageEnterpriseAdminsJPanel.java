    public void populate() {
        networksjComboBox.removeAllItems();
        enterprisetypejComboBox.removeAllItems();
        enterprisenamejComboBox.removeAllItems();
        for (Network network : system.getNetworkList()) {
            networksjComboBox.addItem(network);
        }
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            enterprisetypejComboBox.addItem(type);
        }
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                enterprisenamejComboBox.addItem(enterprise);
            }
        }
    }

    public void populatetable() {
        DefaultTableModel dtm = (DefaultTableModel) adminsjTable.getModel();
        dtm.setRowCount(0);

        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount ua : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = network.getName();
                    row[2] = ua.getUsername();
                    dtm.addRow(row);
                }
            }
        }
    }

    public void populateenterprise(Network network, Enterprise.EnterpriseType type) {
        enterprisenamejComboBox.removeAllItems();

        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {

            if (enterprise.getEnterpriseType().equals(type)) {
                enterprisenamejComboBox.addItem(enterprise);
            }
        }

    }

