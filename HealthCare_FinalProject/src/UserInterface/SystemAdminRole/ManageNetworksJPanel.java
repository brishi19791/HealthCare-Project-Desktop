    public void populatenetwork() {
        DefaultTableModel dtm = (DefaultTableModel) networkjTable.getModel();
        dtm.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            Object[] row = new Object[1];
            row[0] = network.getName();
            dtm.addRow(row);
        }
    }

