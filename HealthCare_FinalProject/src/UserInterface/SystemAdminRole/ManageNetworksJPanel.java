    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (networkTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Network Name");
        } else {
            String networkName = networkTextField1.getText();
            for(Network n:system.getNetworkList()){
                if(networkName.equals(n.getName())){
                    JOptionPane.showMessageDialog(null,"Network with given name exists!!");
                    return;
                }
                    
            }
            Network network = system.createNetwork();
            network.setName(networkName);
            populatenetwork();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

