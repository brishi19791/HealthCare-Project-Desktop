    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String userName = nameJTextField.getText();
        String password = passwordJTextField.getText();
         if(userName.isEmpty()||password.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter details");
            return;
        }
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        Person person = (Person) employeeJComboBox.getSelectedItem();
        Role role = (Role) roleJComboBox.getSelectedItem();
        
        for(Network network:system.getNetworkList()){
                for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
                     for(Organization o:enterprise.getOrganizationDirectory().getOrganizationList()){
                       for(UserAccount ua:o.getUserAccountDirectory().getUserAccountList()){
                           if(ua.getUsername().equals(userName)){
                               JOptionPane.showMessageDialog(null, "A user with same username already exists");
                               return;
                           }
                       }
                    }
                }
            }

        organization.getUserAccountDirectory().createUserAccount(userName, password, person, role);

        popData();
    }//GEN-LAST:event_createUserJButtonActionPerformed

