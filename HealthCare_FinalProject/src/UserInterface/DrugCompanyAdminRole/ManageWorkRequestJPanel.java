    public void populateTable(){
         DefaultTableModel dtm=(DefaultTableModel)pharmacistreqdetailsjTable.getModel();
        dtm.setRowCount(0);
        
        for(WorkRequest request:enterprise.getWorkQueue().getWorkRequestList()){
            Object[] row=new Object[6];
            row[0]=request;
            row[1]=request.getSender();
            row[2]=request.getReceiver()==null?null:request.getReceiver();
            PharmacistWorkRequest req=(PharmacistWorkRequest)request;
            row[3]=req.getDrug();
            row[4]=request.getStatus();
            row[5]=req.getPharmacy();
            dtm.addRow(row);
        }
    }

