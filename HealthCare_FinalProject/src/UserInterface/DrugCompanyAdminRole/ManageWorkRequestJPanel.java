    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow=pharmacistreqdetailsjTable.getSelectedRow();

        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a Work Request");
        }
        PharmacistWorkRequest request=(PharmacistWorkRequest)pharmacistreqdetailsjTable.getValueAt(selectedRow,0);
        request.setStatus("Approved");
        request.setReceiver(account);
        Organization org=null;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (o instanceof DrugDistributionOrganization) {
                    org = o;
                    break;
                }
            }
        if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(request);
                account.getWorkQueue().getWorkRequestList().add(request);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

